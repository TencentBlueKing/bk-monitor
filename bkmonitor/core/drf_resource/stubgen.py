@dataclass
class StubRenderContext:
    type_defs: list[str] = field(default_factory=list)
    generated_type_defs: set[str] = field(default_factory=set)


def _serializer_instance(
    serializer_obj_or_cls: serializers.BaseSerializer | type[Any] | None,
) -> serializers.BaseSerializer | None:
    if serializer_obj_or_cls is None:
        return None
    if isinstance(serializer_obj_or_cls, serializers.BaseSerializer):
        return serializer_obj_or_cls
    if inspect.isclass(serializer_obj_or_cls):
        try:
            instance = serializer_obj_or_cls()
        except Exception:  # pylint: disable=broad-except
            return None
        return instance if isinstance(instance, serializers.BaseSerializer) else None
    return None


def _static_getattr(node: Any, name: str, default: Any = None) -> Any:
    try:
        return object.__getattribute__(node, name)
    except AttributeError:
        pass

    try:
        return inspect.getattr_static(node, name)
    except AttributeError:
        return default


def _ensure_loaded(node: Any) -> bool:
    loaded = _static_getattr(node, "loaded", True)
    if loaded:
        return True
    setup = _static_getattr(node, "_setup", None)
    if callable(setup):
        try:
            setup()
        except Exception as err:  # pylint: disable=broad-except
            logger.warning("skip stub generation for unloaded resource node %r, error: %s", node, err)
            return False
    return True


def _public_names(node: Any) -> list[str]:
    namespace = getattr(node, "__dict__", None)
    if namespace:
        return sorted(name for name in namespace if not name.startswith("_"))
    return sorted(name for name in dir(node) if not name.startswith("_"))


def _is_namespace_node(node: Any) -> bool:
    if inspect.isfunction(node) or inspect.ismethod(node) or inspect.isclass(node):
        return False
    if _static_getattr(node, "_methods", None) is not None:
        return True
    return hasattr(node, "__dict__")


def _collect_entrypoint_tree(node: Any) -> dict[str, Any]:
    if not _ensure_loaded(node):
        return {}

    methods = _static_getattr(node, "_methods", None)
    package = _static_getattr(node, "_package", None)
    package_name = getattr(package, "__name__", None)
    if methods is not None:
        tree: dict[str, Any] = {}
        for name, method in sorted(methods.items()):
            if inspect.isfunction(method) and package_name and getattr(method, "__module__", None) != package_name:
                continue
            tree[name] = {
                "kind": FUNCTION_LEAF if inspect.isfunction(method) else RESOURCE_LEAF,
                "object": method,
            }

        for name in _public_names(node):
            if name in tree:
                continue
            child = getattr(node, name)
            if not _is_namespace_node(child):
                continue
            child_tree = _collect_entrypoint_tree(child)
            if child_tree:
                tree[name] = child_tree
        return tree

    tree = {}
    for name in _public_names(node):
        child = getattr(node, name)
        if inspect.isfunction(child):
            if package_name and getattr(child, "__module__", None) != package_name:
                continue
            tree[name] = {
                "kind": FUNCTION_LEAF,
                "object": child,
            }
            continue
        if not _is_namespace_node(child):
            continue
        child_tree = _collect_entrypoint_tree(child)
        if child_tree:
            tree[name] = child_tree
    return tree


def _safe_name(value: str) -> str:
    normalized = re.sub(r"\W+", "_", value).strip("_")
    if not normalized:
        normalized = "value"
    if normalized[0].isdigit():
        normalized = f"_{normalized}"
    if keyword.iskeyword(normalized):
        normalized = f"{normalized}_"
    return normalized


def _class_name(path: tuple[str, ...]) -> str:
    return "_" + "_".join(_safe_name(item) for item in path)


def _source_reference(obj: Any) -> str | None:
    try:
        source_file = inspect.getsourcefile(obj) or inspect.getfile(obj)
    except (OSError, TypeError):
        source_file = None

    if not source_file:
        return None

    source_path = Path(source_file).resolve()
    try:
        display_path = source_path.relative_to(PROJECT_ROOT).as_posix()
    except ValueError:
        display_path = str(source_path)

    qualname = getattr(obj, "__qualname__", getattr(obj, "__name__", None))
    return f"{display_path}:{qualname}" if qualname else display_path


def _resource_serializer_classes(resource_cls: type[Any]) -> tuple[type[Any] | None, type[Any] | None]:
    if hasattr(resource_cls, "_search_serializer_class"):
        try:
            return resource_cls._search_serializer_class()
        except Exception:  # pylint: disable=broad-except
            return None, None

    return getattr(resource_cls, "RequestSerializer", None), getattr(resource_cls, "ResponseSerializer", None)


def _wrap_nullable(type_expr: str, allow_null: bool) -> str:
    return f"{type_expr} | None" if allow_null else type_expr


def _choice_type_expr(field_obj: serializers.ChoiceField) -> str:
    choices = list(getattr(field_obj, "choices", {}).keys())
    if not choices:
        return "Any"
    if all(isinstance(choice, str | int | bool) for choice in choices):
        literal_values = ", ".join(repr(choice) for choice in choices)
        return f"Literal[{literal_values}]"
    return "Any"


def _choice_doc_type_expr(field_obj: serializers.ChoiceField) -> str:
    choices = list(getattr(field_obj, "choices", {}).keys())
    if not choices:
        return "Any"
    if all(isinstance(choice, bool) for choice in choices):
        return "bool"
    if all(isinstance(choice, int) and not isinstance(choice, bool) for choice in choices):
        return "int"
    return "str"


def _typed_dict_block(type_name: str, fields: list[tuple[str, str, bool]]) -> str:
    if not fields:
        body = "    {}"
    else:
        rows = []
        for field_name, field_type, is_required in fields:
            wrapper = "Required" if is_required else "NotRequired"
            rows.append(f'        "{field_name}": {wrapper}[{field_type}],')
        body = "    {\n" + "\n".join(rows) + "\n    }"

    return f'{type_name} = TypedDict(\n    "{type_name}",\n{body},\n    total=False,\n)\n'


def _safe_doc_text(value: Any) -> str:
    text = str(value).replace('"""', r"\"\"\"").strip()
    return re.sub(r"\s+", " ", text)


def _render_default_value(value: Any) -> str | None:
    if value is empty:
        return None
    if callable(value):
        return None
    if value in ("", [], {}):
        return None
    return _safe_doc_text(repr(value))


def _normalized_words(value: str) -> tuple[str, ...]:
    return tuple(filter(None, re.split(r"[^a-z0-9]+", value.lower())))


def _meaningful_label(field_name: str, label: Any) -> str | None:
    if not label:
        return None
    label_text = _safe_doc_text(label)
    if _normalized_words(field_name) == _normalized_words(label_text):
        return None
    return label_text


def _iter_documented_fields(
    serializer_obj_or_cls: serializers.BaseSerializer | type[Any] | None, mode: str
) -> list[tuple[str, serializers.Field]]:
    serializer_obj = _serializer_instance(serializer_obj_or_cls)
    if serializer_obj is None:
        return []

    if isinstance(serializer_obj, serializers.ListSerializer):
        serializer_obj = serializer_obj.child

    if not isinstance(serializer_obj, serializers.Serializer):
        return []

    documented_fields: list[tuple[str, serializers.Field]] = []
    for field_name, field_obj in serializer_obj.fields.items():
        if isinstance(field_obj, serializers.HiddenField):
            continue
        if mode == "request" and getattr(field_obj, "read_only", False):
            continue
        if mode == "response" and getattr(field_obj, "write_only", False):
            continue
        documented_fields.append((field_name, field_obj))
    return documented_fields


def _pascal_case(value: str) -> str:
    chunks = re.split(r"[^a-zA-Z0-9]+", value)
    return "".join(chunk[:1].upper() + chunk[1:] for chunk in chunks if chunk) or "Value"


def _doc_model_name(path: tuple[str, ...], *, is_list: bool) -> str:
    base = "".join(_pascal_case(part) for part in path) or "Payload"
    return f"{base}Item" if is_list else f"{base}Model"


def _field_doc_line(field_name: str, field_obj: serializers.Field, field_path: tuple[str, ...]) -> str:
    field_type = _field_doc_type_expr(field_obj, field_path)
    parts: list[str] = []
    label = _meaningful_label(field_name, getattr(field_obj, "label", None))
    if label:
        parts.append(label)

    help_text = getattr(field_obj, "help_text", None)
    if help_text:
        parts.append(_safe_doc_text(help_text))

    is_required = bool(getattr(field_obj, "required", False) and getattr(field_obj, "default", empty) is empty)
    parts.append("必填" if is_required else "可选")

    default_value = _render_default_value(getattr(field_obj, "default", empty))
    if default_value is not None:
        parts.append(f"默认值: {default_value}")

    choices = list(getattr(field_obj, "choices", {}).keys()) if hasattr(field_obj, "choices") else []
    if choices:
        rendered_choices = ", ".join(_safe_doc_text(repr(choice)) for choice in choices)
        parts.append(f"可选值: {rendered_choices}")

    return f"{field_name} ({field_type}): {'；'.join(parts)}"


def _response_doc_line(field_name: str, field_obj: serializers.Field, field_path: tuple[str, ...]) -> str:
    field_type = _field_doc_type_expr(field_obj, field_path)
    parts: list[str] = []
    label = _meaningful_label(field_name, getattr(field_obj, "label", None))
    if label:
        parts.append(label)

    help_text = getattr(field_obj, "help_text", None)
    if help_text:
        parts.append(_safe_doc_text(help_text))

    if getattr(field_obj, "allow_null", False):
        parts.append("可为空")

    choices = list(getattr(field_obj, "choices", {}).keys()) if hasattr(field_obj, "choices") else []
    if choices:
        rendered_choices = ", ".join(_safe_doc_text(repr(choice)) for choice in choices)
        parts.append(f"可选值: {rendered_choices}")

    if not parts:
        return f"{field_name} ({field_type})"
    return f"{field_name} ({field_type}): {'；'.join(parts)}"


def _nested_serializer(field_obj: serializers.Field) -> serializers.Serializer | None:
    if isinstance(field_obj, serializers.ListSerializer) and isinstance(field_obj.child, serializers.Serializer):
        return field_obj.child
    if isinstance(field_obj, serializers.Serializer):
        return field_obj
    return None


def _field_doc_type_expr(field_obj: serializers.Field, field_path: tuple[str, ...]) -> str:
    if isinstance(field_obj, serializers.ListSerializer):
        if isinstance(field_obj.child, serializers.Serializer):
            return f"list[{_doc_model_name(field_path, is_list=True)}]"
        return f"list[{_field_doc_type_expr(field_obj.child, field_path + ('item',))}]"

    if isinstance(field_obj, serializers.ListField):
        child = getattr(field_obj, "child", None)
        child_type = _field_doc_type_expr(child, field_path + ("item",)) if child else "Any"
        return _wrap_nullable(f"list[{child_type}]", getattr(field_obj, "allow_null", False))

    if isinstance(field_obj, serializers.MultipleChoiceField):
        return _wrap_nullable(f"list[{_choice_doc_type_expr(field_obj)}]", getattr(field_obj, "allow_null", False))

    if isinstance(field_obj, serializers.ChoiceField):
        return _wrap_nullable(_choice_doc_type_expr(field_obj), getattr(field_obj, "allow_null", False))

    if isinstance(field_obj, serializers.Serializer):
        return _wrap_nullable(_doc_model_name(field_path, is_list=False), getattr(field_obj, "allow_null", False))

    if isinstance(field_obj, serializers.DictField | serializers.HStoreField | serializers.JSONField):
        return _wrap_nullable("dict[str, Any]", getattr(field_obj, "allow_null", False))

    if isinstance(field_obj, serializers.BooleanField):
        return _wrap_nullable("bool", getattr(field_obj, "allow_null", False))

    if isinstance(field_obj, serializers.IntegerField):
        return _wrap_nullable("int", getattr(field_obj, "allow_null", False))

    if isinstance(field_obj, serializers.FloatField | serializers.DecimalField):
        return _wrap_nullable("float", getattr(field_obj, "allow_null", False))

    if isinstance(
        field_obj,
        serializers.CharField
        | serializers.DateField
        | serializers.DateTimeField
        | serializers.DurationField
        | serializers.EmailField
        | serializers.FileField
        | serializers.ImageField
        | serializers.IPAddressField
        | serializers.RegexField
        | serializers.SlugField
        | serializers.TimeField
        | serializers.URLField
        | serializers.UUIDField,
    ):
        return _wrap_nullable("str", getattr(field_obj, "allow_null", False))

    if isinstance(field_obj, serializers.RelatedField):
        return _wrap_nullable("Any", getattr(field_obj, "allow_null", False))

    return _wrap_nullable("Any", getattr(field_obj, "allow_null", False))


def _own_docstring(obj: Any) -> str | None:
    namespace = getattr(obj, "__dict__", None)
    if isinstance(namespace, dict) and "__doc__" in namespace:
        return namespace["__doc__"]
    return getattr(obj, "__doc__", None)


def _docstring_lines(obj: Any) -> list[str]:
    doc = _own_docstring(obj)
    if not doc:
        return []
    cleaned_doc = inspect.cleandoc(doc)
    return [_safe_doc_text(line) if line.strip() else "" for line in cleaned_doc.splitlines()]


def _collect_nested_doc_models(
    serializer_obj_or_cls: serializers.BaseSerializer | type[Any] | None, mode: str
) -> list[tuple[str, list[str]]]:
    serializer_obj = _serializer_instance(serializer_obj_or_cls)
    if serializer_obj is None:
        return []

    if isinstance(serializer_obj, serializers.ListSerializer):
        serializer_obj = serializer_obj.child

    if not isinstance(serializer_obj, serializers.Serializer):
        return []

    collected: list[tuple[str, list[str]]] = []
    seen: set[str] = set()

    def walk_field(field_obj: serializers.Field, field_path: tuple[str, ...]) -> None:
        nested_serializer: serializers.Serializer | None = None
        is_list = False

        if isinstance(field_obj, serializers.ListSerializer) and isinstance(field_obj.child, serializers.Serializer):
            nested_serializer = field_obj.child
            is_list = True
        elif isinstance(field_obj, serializers.Serializer):
            nested_serializer = field_obj

        if nested_serializer is None:
            return

        model_name = _doc_model_name(field_path, is_list=is_list)
        if model_name in seen:
            return
        seen.add(model_name)

        model_fields = _iter_documented_fields(nested_serializer, mode=mode)
        model_lines = [
            _field_doc_line(field_name, nested_field_obj, field_path + (field_name,))
            if mode == "request"
            else _response_doc_line(field_name, nested_field_obj, field_path + (field_name,))
            for field_name, nested_field_obj in model_fields
        ]
        collected.append((model_name, model_lines))

        for nested_field_name, nested_field_obj in model_fields:
            walk_field(nested_field_obj, field_path + (nested_field_name,))

    for field_name, field_obj in _iter_documented_fields(serializer_obj, mode=mode):
        walk_field(field_obj, (field_name,))

    return collected


def _request_tree_lines(serializer_obj_or_cls: serializers.BaseSerializer | type[Any] | None) -> list[str]:
    serializer_obj = _serializer_instance(serializer_obj_or_cls)
    if serializer_obj is None:
        return []

    if isinstance(serializer_obj, serializers.ListSerializer):
        serializer_obj = serializer_obj.child

    if not isinstance(serializer_obj, serializers.Serializer):
        return []

    def walk(
        fields: list[tuple[str, serializers.Field]],
        prefix: str,
        field_path_prefix: tuple[str, ...],
    ) -> list[str]:
        lines: list[str] = []
        total = len(fields)
        for index, (field_name, field_obj) in enumerate(fields):
            is_last = index == total - 1
            branch = "└── " if is_last else "├── "
            child_prefix = prefix + ("    " if is_last else "│   ")
            current_path = field_path_prefix + (field_name,)
            lines.append(f"{prefix}{branch}{_field_doc_line(field_name, field_obj, current_path)}")

            nested = _nested_serializer(field_obj)
            if nested is None:
                continue

            nested_fields = _iter_documented_fields(nested, mode="request")
            if nested_fields:
                lines.extend(walk(nested_fields, child_prefix, current_path))

        return lines

    return walk(_iter_documented_fields(serializer_obj, mode="request"), "", ())


def _resource_docstring_lines(resource_cls: type[Any], response_serializer_cls: type[Any] | None) -> list[str]:
    lines = _docstring_lines(resource_cls)

    request_serializer_cls, _ = _resource_serializer_classes(resource_cls)
    request_tree = _request_tree_lines(request_serializer_cls)
    if request_tree:
        if lines:
            lines.append("")
        lines.append("Args:")
        lines.extend(f"    {line}" for line in request_tree)

    response_fields = _iter_documented_fields(response_serializer_cls, mode="response")
    if response_fields:
        if lines:
            lines.append("")
        lines.append("Returns:")
        lines.append("    响应数据。")
        lines.extend(
            f"        {_response_doc_line(field_name, field_obj, (field_name,))}"
            for field_name, field_obj in response_fields
        )

    response_models = _collect_nested_doc_models(response_serializer_cls, mode="response")
    if response_models:
        if lines:
            lines.append("")
        lines.append("Response Models:")
        for model_name, model_lines in response_models:
            lines.append(f"    {model_name}:")
            lines.extend(f"        {line}" for line in model_lines)

    return lines


def _function_docstring_lines(function_obj: Any) -> list[str]:
    return _docstring_lines(function_obj)


def _render_docstring_block(lines: list[str], indent: str = "        ") -> list[str]:
    if not lines:
        return []

    rendered = [f'{indent}"""']
    rendered.extend(f"{indent}{line}" if line else indent for line in lines)
    rendered.append(f'{indent}"""')
    return rendered


def _field_type_expr(field_obj: serializers.Field, type_name: str, context: StubRenderContext, mode: str) -> str:
    if isinstance(field_obj, serializers.ListSerializer):
        child_type = _field_type_expr(field_obj.child, f"{type_name}_item", context, mode)
        return _wrap_nullable(f"list[{child_type}]", getattr(field_obj, "allow_null", False))

    if isinstance(field_obj, serializers.ListField):
        child = getattr(field_obj, "child", None)
        child_type = _field_type_expr(child, f"{type_name}_item", context, mode) if child else "Any"
        return _wrap_nullable(f"list[{child_type}]", getattr(field_obj, "allow_null", False))

    if isinstance(field_obj, serializers.MultipleChoiceField):
        return _wrap_nullable(f"list[{_choice_type_expr(field_obj)}]", getattr(field_obj, "allow_null", False))

    if isinstance(field_obj, serializers.ChoiceField):
        return _wrap_nullable(_choice_type_expr(field_obj), getattr(field_obj, "allow_null", False))

    if isinstance(field_obj, serializers.Serializer):
        nested_type = _serializer_type_expr(field_obj, type_name, context, mode, many=False)
        return _wrap_nullable(nested_type, getattr(field_obj, "allow_null", False))

    if isinstance(field_obj, serializers.DictField | serializers.HStoreField | serializers.JSONField):
        return _wrap_nullable("dict[str, Any]", getattr(field_obj, "allow_null", False))

    if isinstance(field_obj, serializers.BooleanField):
        return _wrap_nullable("bool", getattr(field_obj, "allow_null", False))

    if isinstance(field_obj, serializers.IntegerField):
        return _wrap_nullable("int", getattr(field_obj, "allow_null", False))

    if isinstance(field_obj, serializers.FloatField | serializers.DecimalField):
        return _wrap_nullable("float", getattr(field_obj, "allow_null", False))

    if isinstance(
        field_obj,
        serializers.CharField
        | serializers.DateField
        | serializers.DateTimeField
        | serializers.DurationField
        | serializers.EmailField
        | serializers.FileField
        | serializers.ImageField
        | serializers.IPAddressField
        | serializers.RegexField
        | serializers.SlugField
        | serializers.TimeField
        | serializers.URLField
        | serializers.UUIDField,
    ):
        return _wrap_nullable("str", getattr(field_obj, "allow_null", False))

    if isinstance(field_obj, serializers.RelatedField):
        return _wrap_nullable("Any", getattr(field_obj, "allow_null", False))

    return _wrap_nullable("Any", getattr(field_obj, "allow_null", False))


def _serializer_type_expr(
    serializer_obj_or_cls: serializers.BaseSerializer | type[Any] | None,
    type_name: str,
    context: StubRenderContext,
    mode: str,
    many: bool,
) -> str:
    if serializer_obj_or_cls is None:
        return "Any"

    serializer_obj = _serializer_instance(serializer_obj_or_cls)
    if serializer_obj is None:
        return "Any"

    if isinstance(serializer_obj, serializers.ListSerializer):
        child_type = _field_type_expr(serializer_obj.child, f"{type_name}_item", context, mode)
        return f"list[{child_type}]"

    if not isinstance(serializer_obj, serializers.Serializer):
        return "Any"

    if type_name not in context.generated_type_defs:
        context.generated_type_defs.add(type_name)
        typed_fields = []

        for field_name, field_obj in serializer_obj.fields.items():
            if isinstance(field_obj, serializers.HiddenField):
                continue
            if mode == "request" and getattr(field_obj, "read_only", False):
                continue
            if mode == "response" and getattr(field_obj, "write_only", False):
                continue

            field_type = _field_type_expr(field_obj, f"{type_name}_{_safe_name(field_name)}", context, mode)
            is_required = bool(getattr(field_obj, "required", False) and getattr(field_obj, "default", empty) is empty)
            typed_fields.append((field_name, field_type, is_required))

        context.type_defs.append(_typed_dict_block(type_name, typed_fields))

    return f"list[{type_name}]" if many else type_name


def _resource_request_response_types(
    resource_cls: type[Any],
    path: tuple[str, ...],
    context: StubRenderContext,
) -> tuple[str, str]:
    request_serializer_cls, response_serializer_cls = _resource_serializer_classes(resource_cls)
    request_type = _serializer_type_expr(
        request_serializer_cls,
        _class_name(path + ("Request",)),
        context,
        mode="request",
        many=bool(getattr(resource_cls, "many_request_data", False)),
    )
    response_type = _serializer_type_expr(
        response_serializer_cls,
        _class_name(path + ("Response",)),
        context,
        mode="response",
        many=bool(getattr(resource_cls, "many_response_data", False)),
    )
    return request_type, response_type


def _can_unpack_kwargs(request_type: str, context: StubRenderContext) -> bool:
    if request_type == "Any":
        return True
    return request_type in context.generated_type_defs


def _render_leaf(name: str, leaf_meta: dict[str, Any], path: tuple[str, ...], context: StubRenderContext) -> str:
    lines: list[str] = []
    source_ref = _source_reference(leaf_meta["object"])
    if source_ref:
        lines.append(f"    # source: {source_ref}")

    if leaf_meta["kind"] == RESOURCE_LEAF:
        resource_cls = leaf_meta["object"]
        _, response_serializer_cls = _resource_serializer_classes(resource_cls)
        request_type, response_type = _resource_request_response_types(resource_cls, path + (name,), context)
        lines.extend(
            [
                "    @overload",
                f"    def {name}(self, request_data: {request_type} | None = ...) -> {response_type}: ...",
            ]
        )
        if _can_unpack_kwargs(request_type, context):
            kwargs_type = f"Unpack[{request_type}]" if request_type != "Any" else "Any"
            lines.extend(
                [
                    "    @overload",
                    f"    def {name}(self, **kwargs: {kwargs_type}) -> {response_type}: ...",
                ]
            )
        lines.append(f"    def {name}(self, *args: Any, **kwargs: Any) -> {response_type}:")
        lines.extend(_render_docstring_block(_resource_docstring_lines(resource_cls, response_serializer_cls)))
        lines.append("        ...")
        return "\n".join(lines)

    lines.append(f"    def {name}(self, *args: Any, **kwargs: Any) -> Any:")
    lines.extend(_render_docstring_block(_function_docstring_lines(leaf_meta["object"])))
    lines.append("        ...")
    return "\n".join(lines)


def _render_classes(tree: dict[str, Any], path: tuple[str, ...], context: StubRenderContext) -> list[str]:
    class_name = _class_name(path)
    nested_blocks: list[str] = []
    body_lines: list[str] = []

    for name, value in sorted(tree.items()):
        if isinstance(value, dict):
            if "kind" in value:
                body_lines.append(_render_leaf(name, value, path, context))
            else:
                nested_path = path + (name,)
                nested_blocks.extend(_render_classes(value, nested_path, context))
                body_lines.append(f"    {name}: {_class_name(nested_path)}")
        else:
            body_lines.append(f"    {name}: Any")

    if not body_lines:
        body_lines.append("    ...")

    class_block = "\n".join([f"class {class_name}:"] + body_lines)
    return nested_blocks + [class_block]


def render_entrypoint_type_stubs(root_objects: dict[str, Any]) -> str:
    """
    根据 resource/api 根对象生成 ``core.drf_resource`` 的 pyi 内容。
    """

    context = StubRenderContext()
    rendered_blocks = [STUB_HEADER.rstrip()]
    exported_roots: set[str] = set()
    root_trees: dict[str, dict[str, Any]] = {}

    for root_name, root_object in root_objects.items():
        root_trees[root_name] = _collect_entrypoint_tree(root_object)
        exported_roots.add(root_name)

    for root_name, tree in root_trees.items():
        rendered_blocks.extend(_render_classes(tree, (root_name,), context))
        rendered_blocks.append("")
        rendered_blocks.append(f"{root_name}: {_class_name((root_name,))}")
        exported_roots.add(root_name)

    if context.type_defs:
        rendered_blocks = [STUB_HEADER.rstrip(), *context.type_defs, "", *rendered_blocks[1:]]

    if "resource" not in exported_roots:
        rendered_blocks.append("resource: Any")
    if "api" not in exported_roots:
        rendered_blocks.append("api: Any")
    if "adapter" not in exported_roots:
        rendered_blocks.append("adapter: Any")

    return "\n".join(rendered_blocks).rstrip() + "\n"


def generate_entrypoint_type_stubs(
    output_path: str | Path | None = None,
    root_objects: dict[str, Any] | None = None,
) -> Path:
    """
    为 ``core.drf_resource`` 的 ``resource`` / ``api`` 调用入口生成 type stubs。

    :param output_path: 输出 pyi 文件路径，默认写入 ``core/drf_resource/__init__.pyi``
    :param root_objects: 可选，自定义根对象，便于测试或局部生成
    :return: 最终写入的 pyi 文件路径
    """

    if root_objects is None:
        from core.drf_resource.management.root import api, resource, setup

        setup()
        root_objects = {
            "resource": resource,
            "api": api,
        }

    if output_path:
        target_path = Path(output_path)
    else:
        target_path = PROJECT_ROOT / "typings" / "core" / "drf_resource" / "__init__.pyi"

    target_path.parent.mkdir(parents=True, exist_ok=True)
    target_path.write_text(render_entrypoint_type_stubs(root_objects), encoding="utf-8")
    return target_path
