import { r } from './regex-call';

// const v4text = '(?:25[0-5]|2[0-4][0-9]|1[0-9][0-9]|[1-9][0-9]|[0-9])(?:\\.(?:25[0-5]|2[0-4][0-9]|1[0-9][0-9]|[1-9][0-9]|[0-9])){3}';
// const v6text = '(?:(?:[0-9a-fA-F:]){1,4}(?:(?::(?:[0-9a-fA-F]){1,4}|:)){2,7})+';

const v4Inner = r().a('25[0-5]').or('2[0-4][0-9]').or('1[0-9][0-9]').or('[1-9][0-9]').or('[0-9]').p();

const v6Inner = r()
  .p('[0-9a-fA-F:]', 1, 4)
  .a(r(':').p('[0-9a-fA-F]', 1, 4).or(':').p().p(0, 2, 7))
  .p(0, '+');

export const v4 = v4Inner.a(r('\\.').a(v4Inner).p(0, 3));

export const v6 = v6Inner;
